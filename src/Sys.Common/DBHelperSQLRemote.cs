        /// 有参ExecuteScalar方法
        /// </summary>
        /// <param name="cmdText">预处理的sql文本</param>
        /// <param name="cmdType">预处理的sql文本类型</param>
        /// <param name="prms">预处理的sql文本参数</param>
        /// <returns>首行结果</returns>
        public static object ExecuteScalar(string cmdText, CommandType cmdType, params SqlParameter[] prms)
        {
            using (var conn = new SqlConnection(ConnectionString))
            {
                try
                {
                    var cmd = new SqlCommand(cmdText, conn) { CommandType = cmdType };
                    cmd.Parameters.AddRange(prms);
                    conn.Open();
                    return cmd.ExecuteScalar();
                }
                catch (Exception ex)
                {
                    conn.Dispose();
                    throw new Exception("执行ExecuteScalar方法错误" + ex.Message);
                }
            }
        }




